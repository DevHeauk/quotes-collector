NATIONALITY_FIX = {
    "미국": "US",
    "영국": "GB",
    "중국": "CN",
    "호주": "AU",
    "터키": "TR",
}


def run(apply=False):
    conn = get_db_connection()
    cur = conn.cursor()

    mode = "APPLY" if apply else "DRY-RUN"
    print(f"=== 태그 정리 [{mode}] ===\n")

    # ── 마스터 목록 로드 ──
    cur.execute("SELECT id, name FROM keywords")
    kw_name_to_id = {r[1]: r[0] for r in cur.fetchall()}

    cur.execute("SELECT id, name FROM situations")
    sit_name_to_id = {r[1]: r[0] for r in cur.fetchall()}

    # ── Step 1: 새 키워드 추가 ──
    added_kw = 0
    for name, group in NEW_KEYWORDS.items():
        if name not in kw_name_to_id:
            new_id = str(uuid.uuid4())
            print(f"  [+KW] '{name}' → [{group}]")
            if apply:
                cur.execute(
                    "INSERT INTO keywords (id, name, group_name) VALUES (%s, %s, %s)",
                    (new_id, name, group),
                )
            kw_name_to_id[name] = new_id
            added_kw += 1
    print(f"  → 새 키워드 {added_kw}개 {'추가' if apply else '추가 예정'}\n")

    # ── Step 2: 새 상황 추가 ──
    added_sit = 0
    for name, group in NEW_SITUATIONS.items():
        if name not in sit_name_to_id:
            new_id = str(uuid.uuid4())
            print(f"  [+SIT] '{name}' → [{group}]")
            if apply:
                cur.execute(
                    "INSERT INTO situations (id, name, group_name) VALUES (%s, %s, %s)",
                    (new_id, name, group),
                )
            sit_name_to_id[name] = new_id
            added_sit += 1
    print(f"  → 새 상황 {added_sit}개 {'추가' if apply else '추가 예정'}\n")

    # ── Step 3: 전체 명언 처리 ──
    cur.execute("SELECT id, keywords, situation FROM quotes")
    quotes = cur.fetchall()

    format_fixed = 0
    kw_remapped = 0
    sit_remapped = 0
    kw_id_synced = 0
    sit_id_synced = 0
    unmapped_kw = set()
    unmapped_sit = set()

    for qid, kws, sits in quotes:
        new_kws = []
        new_sits = []
        changed = False

        # ── Keywords 처리 ──
        if isinstance(kws, list):
            for k in kws:
                if isinstance(k, dict) and "name" in k:
                    # dict → string
                    name = k["name"]
                    format_fixed += 1
                    changed = True
                else:
                    name = k

                # 리매핑
                if name in KEYWORD_REMAP:
                    new_name = KEYWORD_REMAP[name]
                    kw_remapped += 1
                    changed = True
                    name = new_name

                if name not in kw_name_to_id:
                    unmapped_kw.add(name)

                new_kws.append(name)

        # 중복 제거
        seen = set()
        deduped_kws = []
        for k in new_kws:
            if k not in seen:
                seen.add(k)
                deduped_kws.append(k)
        if len(deduped_kws) != len(new_kws):
            changed = True
        new_kws = deduped_kws

        # ── Situations 처리 ──
        if isinstance(sits, list):
            for s in sits:
                if isinstance(s, dict) and "name" in s:
                    name = s["name"]
                    format_fixed += 1
                    changed = True
                else:
                    name = s

                if name in SITUATION_REMAP:
                    new_name = SITUATION_REMAP[name]
                    sit_remapped += 1
                    changed = True
                    name = new_name

                if name not in sit_name_to_id:
                    unmapped_sit.add(name)

                new_sits.append(name)

        # 중복 제거
        seen = set()
        deduped_sits = []
        for s in new_sits:
            if s not in seen:
                seen.add(s)
                deduped_sits.append(s)
        if len(deduped_sits) != len(new_sits):
            changed = True
        new_sits = deduped_sits

        # ── ID 배열 재생성 ──
        new_kw_ids = [kw_name_to_id[k] for k in new_kws if k in kw_name_to_id]
        new_sit_ids = [sit_name_to_id[s] for s in new_sits if s in sit_name_to_id]

        if changed or True:  # 항상 ID 동기화
            kw_id_synced += 1
            sit_id_synced += 1

            if apply:
                import json as _json

                cur.execute(
                    """UPDATE quotes
                       SET keywords = %s::jsonb,
                           situation = %s::jsonb,
                           keyword_ids = %s,
                           situation_ids = %s
                       WHERE id = %s""",
                    (
                        _json.dumps(new_kws, ensure_ascii=False),
                        _json.dumps(new_sits, ensure_ascii=False),
                        new_kw_ids,
                        new_sit_ids,
                        qid,
                    ),
                )

    print(f"  [형식] dict→string 변환: {format_fixed}건")
    print(f"  [리매핑] 키워드: {kw_remapped}건")
    print(f"  [리매핑] 상황: {sit_remapped}건")
    print(f"  [동기화] keyword_ids: {kw_id_synced}건")
    print(f"  [동기화] situation_ids: {sit_id_synced}건")

    if unmapped_kw:
        print(f"\n  ⚠️  여전히 마스터에 없는 키워드 {len(unmapped_kw)}종:")
        for k in sorted(unmapped_kw):
            print(f"    - \"{k}\"")

    if unmapped_sit:
        print(f"\n  ⚠️  여전히 마스터에 없는 상황 {len(unmapped_sit)}종:")
        for s in sorted(unmapped_sit):
            print(f"    - \"{s}\"")

    # ── Step 4: 국적 코드 변환 ──
    nat_fixed = 0
    for wrong, correct in NATIONALITY_FIX.items():
        cur.execute(
            "SELECT count(*) FROM authors WHERE nationality = %s", (wrong,)
        )
        cnt = cur.fetchone()[0]
        if cnt > 0:
            print(f"\n  [국적] '{wrong}' → '{correct}': {cnt}명")
            nat_fixed += cnt
            if apply:
                cur.execute(
                    "UPDATE authors SET nationality = %s WHERE nationality = %s",
                    (correct, wrong),
                )

    print(f"\n  → 국적 코드 수정: {nat_fixed}명 {'완료' if apply else '예정'}")

    # ── 커밋 ──
    if apply:
        conn.commit()
        print("\n✅ 모든 변경사항이 DB에 반영되었습니다.")
    else:
        conn.rollback()
        print("\n📋 dry-run 완료. 실제 반영하려면 --apply 옵션을 추가하세요.")

    conn.close()


if __name__ == "__main__":
    parser = argparse.ArgumentParser(description="태그 정리 스크립트")
    parser.add_argument("--apply", action="store_true", help="실제 DB에 반영")
    args = parser.parse_args()
    run(apply=args.apply)

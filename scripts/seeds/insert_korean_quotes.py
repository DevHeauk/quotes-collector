def get_connection():
    return psycopg2.connect(**DB_CONFIG)


def get_or_create_profession(cur, name):
    cur.execute("SELECT id FROM professions WHERE name = %s", (name,))
    row = cur.fetchone()
    if row:
        return row[0]
    new_id = str(uuid.uuid4())
    cur.execute(
        "INSERT INTO professions (id, name, group_name) VALUES (%s, %s, %s)",
        (new_id, name, "기타"),
    )
    return new_id


def get_or_create_field(cur, name):
    cur.execute("SELECT id FROM fields WHERE name = %s", (name,))
    row = cur.fetchone()
    if row:
        return row[0]
    new_id = str(uuid.uuid4())
    cur.execute("INSERT INTO fields (id, name) VALUES (%s, %s)", (new_id, name))
    return new_id


def get_or_create_author(cur, name, nationality, birth_year, profession_name, field_name):
    cur.execute("SELECT id FROM authors WHERE name = %s", (name,))
    row = cur.fetchone()
    if row:
        return row[0]
    profession_id = get_or_create_profession(cur, profession_name)
    field_id = get_or_create_field(cur, field_name)
    new_id = str(uuid.uuid4())
    cur.execute(
        """INSERT INTO authors (id, name, nationality, birth_year, profession_id, field_id)
           VALUES (%s, %s, %s, %s, %s, %s)""",
        (new_id, name, nationality, birth_year, profession_id, field_id),
    )
    return new_id


def get_keyword_id(cur, name):
    cur.execute("SELECT id FROM keywords WHERE name = %s", (name,))
    row = cur.fetchone()
    return row[0] if row else None


def get_situation_id(cur, name):
    cur.execute("SELECT id FROM situations WHERE name = %s", (name,))
    row = cur.fetchone()
    return row[0] if row else None


def main():
    conn = get_connection()
    cur = conn.cursor()

    # 1. 저자 생성
    author_map = {}
    for name, nationality, birth_year, profession, field in AUTHORS:
        # 백범 김구 → 김구와 동일인이므로, "백범 김구"를 "김구"로 매핑
        display_name = name
        if name == "백범 김구":
            display_name = "김구"
        author_map[name] = get_or_create_author(
            cur, display_name, nationality, birth_year, profession, field
        )

    inserted = 0
    skipped = 0

    for q in QUOTES:
        text, text_original, original_language, author_name, source, year, kw_names, sit_names = q

        # 저자 ID
        if author_name in author_map:
            author_id = author_map[author_name]
        else:
            # 찾지 못하면 AUTHORS에서 검색
            for a in AUTHORS:
                if a[0] == author_name:
                    author_map[author_name] = get_or_create_author(
                        cur, author_name, a[1], a[2], a[3], a[4]
                    )
                    author_id = author_map[author_name]
                    break
            else:
                print(f"WARNING: 저자 '{author_name}'를 찾을 수 없음. 스킵.")
                skipped += 1
                continue

        # 중복 체크
        cur.execute("SELECT id FROM quotes WHERE text = %s", (text,))
        if cur.fetchone():
            skipped += 1
            continue

        # 키워드
        keyword_ids = []
        keyword_names = []
        for kw in kw_names:
            kid = get_keyword_id(cur, kw)
            if kid:
                keyword_ids.append(kid)
                keyword_names.append(kw)

        # 상황
        situation_ids = []
        situation_names = []
        for sit in sit_names:
            sid = get_situation_id(cur, sit)
            if sid:
                situation_ids.append(sid)
                situation_names.append(sit)

        # source_reliability
        source_reliability = "attributed" if source else "unknown"

        quote_id = str(uuid.uuid4())
        cur.execute(
            """INSERT INTO quotes
               (id, text, text_original, original_language, author_id, source, year,
                keywords, situation, keyword_ids, situation_ids,
                status, source_reliability)
               VALUES (%s, %s, %s, %s, %s, %s, %s,
                       %s::jsonb, %s::jsonb, %s, %s,
                       %s, %s)""",
            (
                quote_id,
                text,
                text_original,
                original_language,
                author_id,
                source,
                year,
                psycopg2.extras.Json(keyword_names),
                psycopg2.extras.Json(situation_names),
                keyword_ids if keyword_ids else None,
                situation_ids if situation_ids else None,
                "draft",
                source_reliability,
            ),
        )
        inserted += 1

    conn.commit()
    cur.close()
    conn.close()

    print(f"=== 완료 ===")
    print(f"저장된 명언: {inserted}개")
    print(f"스킵(중복): {skipped}개")
    print(f"총 시도: {len(QUOTES)}개")


if __name__ == "__main__":
    main()

def get_connection():
    return psycopg2.connect(**DB_CONFIG)


def get_or_create_profession(cur, name="작가", group_name="예술"):
    cur.execute("SELECT id FROM professions WHERE name = %s", (name,))
    row = cur.fetchone()
    if row:
        return row[0]
    new_id = str(uuid.uuid4())
    cur.execute(
        "INSERT INTO professions (id, name, group_name) VALUES (%s, %s, %s)",
        (new_id, name, group_name),
    )
    return new_id


def get_or_create_field(cur, name="문학"):
    cur.execute("SELECT id FROM fields WHERE name = %s", (name,))
    row = cur.fetchone()
    if row:
        return row[0]
    new_id = str(uuid.uuid4())
    cur.execute(
        "INSERT INTO fields (id, name) VALUES (%s, %s)",
        (new_id, name),
    )
    return new_id


def get_or_create_author(cur, name, birth_year, profession_id, field_id):
    cur.execute("SELECT id FROM authors WHERE name = %s", (name,))
    row = cur.fetchone()
    if row:
        return row[0]
    new_id = str(uuid.uuid4())
    cur.execute(
        "INSERT INTO authors (id, name, nationality, birth_year, profession_id, field_id) VALUES (%s, %s, %s, %s, %s, %s)",
        (new_id, name, "KR", birth_year, profession_id, field_id),
    )
    return new_id


def get_keyword_id(cur, name):
    cur.execute("SELECT id FROM keywords WHERE name = %s", (name,))
    row = cur.fetchone()
    if row:
        return row[0]
    return None


def get_situation_id(cur, name):
    cur.execute("SELECT id FROM situations WHERE name = %s", (name,))
    row = cur.fetchone()
    if row:
        return row[0]
    return None


def main():
    conn = get_connection()
    cur = conn.cursor()

    # 1. profession, field 조회/생성
    profession_id = get_or_create_profession(cur)
    field_id = get_or_create_field(cur)

    # 2. 작가 캐시
    author_cache = {}
    for name, birth_year in AUTHORS.items():
        author_cache[name] = get_or_create_author(
            cur, name, birth_year, profession_id, field_id
        )

    # 3. 키워드/상황 캐시
    keyword_cache = {}
    cur.execute("SELECT id, name FROM keywords")
    for row in cur.fetchall():
        keyword_cache[row[1]] = row[0]

    situation_cache = {}
    cur.execute("SELECT id, name FROM situations")
    for row in cur.fetchall():
        situation_cache[row[1]] = row[0]

    # 4. 명언 삽입
    inserted = 0
    skipped = 0

    for text, author_name, source, year, keywords, situations in QUOTES:
        # 중복 체크
        cur.execute("SELECT id FROM quotes WHERE text = %s", (text,))
        if cur.fetchone():
            skipped += 1
            continue

        author_id = author_cache[author_name]

        # 키워드 처리 (이별 등 마스터에 없는 키워드는 제외)
        keyword_names = [k for k in keywords if k in keyword_cache]
        keyword_ids = [keyword_cache[k] for k in keyword_names]

        # 상황 처리
        situation_names = [s for s in situations if s in situation_cache]
        situation_ids = [situation_cache[s] for s in situation_names]

        import json

        keywords_jsonb = json.dumps(keyword_names, ensure_ascii=False)
        situation_jsonb = json.dumps(situation_names, ensure_ascii=False)

        source_reliability = "attributed" if source else "unknown"

        quote_id = str(uuid.uuid4())
        cur.execute(
            """INSERT INTO quotes
            (id, text, text_original, original_language, author_id, source, year,
             keywords, situation, keyword_ids, situation_ids,
             status, source_reliability)
            VALUES (%s, %s, %s, %s, %s, %s, %s, %s, %s, %s, %s, %s, %s)""",
            (
                quote_id,
                text,
                text,  # text_original = 같은 한국어 텍스트
                "ko",
                author_id,
                source,
                year,
                keywords_jsonb,
                situation_jsonb,
                keyword_ids,
                situation_ids,
                "draft",
                source_reliability,
            ),
        )
        inserted += 1

    conn.commit()
    cur.close()
    conn.close()

    print(f"완료! 저장: {inserted}개, 스킵(중복): {skipped}개, 총 시도: {len(QUOTES)}개")


if __name__ == "__main__":
    main()
